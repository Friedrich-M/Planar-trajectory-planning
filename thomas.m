function m=thomas(n,g) %输入参数为矩阵方程的系数矩阵
b=zeros(4,1) %β向量初始化
b(1)=n(1,2)/2 %β向量计算，起始点β1赋值
for i=2:4
    b(i)=n(i,i+1)/(2-n(i,i-1)*b(i-1)) %β向量计算
end %追的过程（1）分解为LU形式
y=zeros(5,1) %y向量初始化
y(1)=g(1)/2 %y向量计算，起始点y1赋值
for i=2:5
y(i)=(g(i)-n(i,i-1)*y(i-1))/(2-n(i,i-1)*b(i-1))
%y向量计算
end %追的过程（2）解方程组Ly= g的递推算式
m=zeros(5,1) %解向量初始化
m(5)=y(5) %解向量计算，起始点Mn赋值
for i=4:-1:1
    m(i)=y(i)-b(i)*m(i+1) %向前代入，解向量计算
end %赶的过程
end %函数结束，返回矩阵方程的解

